#아래 코드는 내가 혼자 해본것

import turtle #터틀 모듈 사용하기 위해
t = turtle.Turtle() #거북이 생성
t.forward(100) #100만큼 앞으로 이동
t.left(125) #왼쪽125도
t.forward(75) #75만큼 이동
t.left(100) #왼쪽 100도
t.forward(85) #85만큼 이동


